def collect_referenced_cpfs(
    parsed: dict[str, list[dict[str, Any]]],
    palestra_seeds: dict[int, dict[str, Any]],
) -> set[str]:
    cpfs: set[str] = set()

    for row in parsed["dados"]:
        cpf = normalize_cpf(row.get("CPF"))
        if cpf:
            cpfs.add(cpf)

    for row in parsed["inscricao"]:
        cpf = normalize_cpf(row.get("CPFdadosFK"))
        if cpf:
            cpfs.add(cpf)

    for row in parsed["participacao_minicurso"]:
        cpf = normalize_cpf(row.get("CPFusuarioFK"))
        if cpf:
            cpfs.add(cpf)

    for row in parsed["participacao_palestra"]:
        cpf = normalize_cpf(row.get("CPFusuarioFK"))
        if cpf:
            cpfs.add(cpf)

    for row in parsed["ministrante_minicurso"]:
        cpf = normalize_cpf(row.get("CPFusuarioFK"))
        if cpf:
            cpfs.add(cpf)

    for seed in palestra_seeds.values():
        cpf = seed.get("speakerCpf")
        if cpf:
            cpfs.add(cpf)

    return cpfs


def map_people(
    legacy_rows: list[dict[str, Any]],
    required_cpfs: set[str],
    fallback_now: datetime,
) -> tuple[list[dict[str, Any]], dict[str, str]]:
    people_by_cpf: dict[str, dict[str, Any]] = {}

    for row in legacy_rows:
        cpf = normalize_cpf(row.get("CPF"))
        if cpf is None:
            continue
        created_at = parse_mysql_datetime(row.get("create_time")) or fallback_now
        people_by_cpf[cpf] = {
            "id": uuid7_string(),
            "name": normalize_person_name(row.get("nome")) or f"Pessoa {cpf}",
            "email": normalize_email(row.get("email")),
            "identityDocument": cpf,
            "academicId": None,
            "externalRef": build_prefixed_id(PREFIX, "legacy-cpf", cpf),
            "createdAt": created_at,
            "updatedAt": fallback_now,
        }

    for cpf in sorted(required_cpfs):
        if cpf in people_by_cpf:
            continue
        people_by_cpf[cpf] = {
            "id": uuid7_string(),
            "name": f"Pessoa {cpf}",
            "email": None,
            "identityDocument": cpf,
            "academicId": None,
            "externalRef": build_prefixed_id(PREFIX, "legacy-cpf", cpf),
            "createdAt": fallback_now,
            "updatedAt": fallback_now,
        }

    cpf_to_person_id = {cpf: row["id"] for cpf, row in people_by_cpf.items()}
    rows = [people_by_cpf[cpf] for cpf in sorted(people_by_cpf)]
    return rows, cpf_to_person_id


def map_major_event_subscriptions(
    legacy_rows: list[dict[str, Any]],
    cpf_to_person_id: dict[str, str],
    major_event_id_by_year_ref: dict[int, str],
    fallback_now: datetime,
) -> tuple[list[dict[str, Any]], int, dict[str, bool]]:
    rows_by_id: dict[str, dict[str, Any]] = {}
    skipped = 0
    paid_by_major_event: dict[str, bool] = {}

    for row in legacy_rows:
        cpf = normalize_cpf(row.get("CPFdadosFK"))
        year_ref_id = decimal_to_int(row.get("idAno_ReferenciaFK"))
        inscription_id = decimal_to_int(row.get("idInscricao"))
        if cpf is None or year_ref_id is None or inscription_id is None:
            skipped += 1
            continue

        person_id = cpf_to_person_id.get(cpf)
        major_event_id = major_event_id_by_year_ref.get(year_ref_id)
        if person_id is None or major_event_id is None:
            skipped += 1
            continue

        amount_paid = decimal_to_int(row.get("total"))
        if amount_paid is not None and amount_paid > 0:
            paid_by_major_event[major_event_id] = True

        status = decimal_to_int(row.get("status")) or 0
        subscription_status = "CONFIRMED" if status == 1 else "WAITING_RECEIPT_UPLOAD"
        created_at = parse_mysql_datetime(row.get("create_time")) or fallback_now

        subscription_id = deterministic_prefixed_id(
            PREFIX, f"legacy2-major-sub:{inscription_id}:{cpf}:{year_ref_id}"
        )
        rows_by_id[subscription_id] = {
            "id": subscription_id,
            "majorEventId": major_event_id,
            "personId": person_id,
            "amountPaid": amount_paid,
            "paymentDate": created_at,
            "paymentTier": None,
            "subscriptionStatus": subscription_status,
            "createdAt": created_at,
            "createdById": None,
        }

    return sorted(rows_by_id.values(), key=lambda item: item["id"]), skipped, paid_by_major_event


def map_event_subscriptions(
    minicurso_rows: list[dict[str, Any]],
    palestra_rows: list[dict[str, Any]],
    cpf_to_person_id: dict[str, str],
    minicurso_seeds: dict[int, dict[str, Any]],
    palestra_seeds: dict[int, dict[str, Any]],
    fallback_now: datetime,
) -> tuple[list[dict[str, Any]], int]:
    rows_by_id: dict[str, dict[str, Any]] = {}
    skipped = 0

    for row in minicurso_rows:
        cpf = normalize_cpf(row.get("CPFusuarioFK"))
        minicurso_id = decimal_to_int(row.get("idMinicursoFK"))
        if cpf is None or minicurso_id is None:
            skipped += 1
            continue
        person_id = cpf_to_person_id.get(cpf)
        seed = minicurso_seeds.get(minicurso_id)
        if person_id is None or seed is None:
            skipped += 1
            continue

        subscription_id = deterministic_prefixed_id(
            PREFIX, f"legacy2-event-sub:minicurso:{cpf}:{minicurso_id}"
        )
        rows_by_id[subscription_id] = {
            "id": subscription_id,
            "eventId": seed["id"],
            "personId": person_id,
            "createdAt": parse_mysql_datetime(row.get("create_time")) or seed["startDate"] or fallback_now,
            "createdById": None,
        }

    for row in palestra_rows:
        cpf = normalize_cpf(row.get("CPFusuarioFK"))
        palestra_id = decimal_to_int(row.get("idPalestraFK"))
        if cpf is None or palestra_id is None:
            skipped += 1
            continue
        person_id = cpf_to_person_id.get(cpf)
        seed = palestra_seeds.get(palestra_id)
        if person_id is None or seed is None:
            skipped += 1
            continue

        subscription_id = deterministic_prefixed_id(
            PREFIX, f"legacy2-event-sub:palestra:{cpf}:{palestra_id}"
        )
        rows_by_id[subscription_id] = {
            "id": subscription_id,
            "eventId": seed["id"],
            "personId": person_id,
            "createdAt": parse_mysql_datetime(row.get("create_time")) or seed["startDate"] or fallback_now,
            "createdById": None,
        }

    return sorted(rows_by_id.values(), key=lambda item: item["id"]), skipped


def map_event_attendances(
    legacy_rows: list[dict[str, Any]],
    cpf_to_person_id: dict[str, str],
    minicurso_seeds: dict[int, dict[str, Any]],
    fallback_now: datetime,
) -> tuple[list[dict[str, Any]], int]:
    rows_by_pair: dict[tuple[str, str], dict[str, Any]] = {}
    skipped = 0

    for row in legacy_rows:
        has_presence = decimal_to_int(row.get("presenca")) == 1
        if not has_presence:
            continue

        cpf = normalize_cpf(row.get("CPFusuarioFK"))
        minicurso_id = decimal_to_int(row.get("idMinicursoFK"))
        if cpf is None or minicurso_id is None:
            skipped += 1
            continue
        person_id = cpf_to_person_id.get(cpf)
        seed = minicurso_seeds.get(minicurso_id)
        if person_id is None or seed is None:
            skipped += 1
            continue

        pair = (person_id, seed["id"])
        attended_at = parse_mysql_datetime(row.get("create_time")) or seed["startDate"] or fallback_now
        rows_by_pair[pair] = {
            "personId": pair[0],
            "eventId": pair[1],
            "attendedAt": attended_at,
            "createdAt": attended_at,
            "createdById": None,
        }

    rows = [rows_by_pair[key] for key in sorted(rows_by_pair)]
    return rows, skipped


def map_event_lecturers(
    ministrante_rows: list[dict[str, Any]],
    palestra_seeds: dict[int, dict[str, Any]],
    cpf_to_person_id: dict[str, str],
    minicurso_seeds: dict[int, dict[str, Any]],
    fallback_now: datetime,
) -> tuple[list[dict[str, Any]], int]:
    rows_by_pair: dict[tuple[str, str], dict[str, Any]] = {}
    skipped = 0

    for row in ministrante_rows:
        cpf = normalize_cpf(row.get("CPFusuarioFK"))
        minicurso_id = decimal_to_int(row.get("idMinicursoFK"))
        if cpf is None or minicurso_id is None:
            skipped += 1
            continue
        person_id = cpf_to_person_id.get(cpf)
        seed = minicurso_seeds.get(minicurso_id)
        if person_id is None or seed is None:
            skipped += 1
            continue
        pair = (seed["id"], person_id)
        created_at = parse_mysql_datetime(row.get("create_time")) or seed["startDate"] or fallback_now
        rows_by_pair[pair] = {
            "eventId": pair[0],
            "personId": pair[1],
            "createdAt": created_at,
            "createdById": None,
        }

    for seed in palestra_seeds.values():
        cpf = seed.get("speakerCpf")
        if not cpf:
            continue
        person_id = cpf_to_person_id.get(cpf)
        if person_id is None:
            skipped += 1
            continue
        pair = (seed["id"], person_id)
        rows_by_pair[pair] = {
            "eventId": pair[0],
            "personId": pair[1],
            "createdAt": seed["createdAt"] or fallback_now,
            "createdById": None,
        }

    rows = [rows_by_pair[key] for key in sorted(rows_by_pair)]
    return rows, skipped


def build_event_groups(now: datetime) -> list[dict[str, Any]]:
    return [
        {
            "id": build_prefixed_id(PREFIX, "event-group", "palestras"),
            "name": "Palestras (SECOMPP legado 2)",
            "type": "PALESTRA",
            "createdAt": now,
            "updatedAt": now,
        },
        {
            "id": build_prefixed_id(PREFIX, "event-group", "minicursos"),
            "name": "Minicursos (SECOMPP legado 2)",
            "type": "MINICURSO",
            "createdAt": now,
            "updatedAt": now,
        },
    ]


def build_event_rows(
    minicurso_seeds: dict[int, dict[str, Any]],
    palestra_seeds: dict[int, dict[str, Any]],
    event_group_id_by_type: dict[str, str],
    fallback_now: datetime,
) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    for seed in [*minicurso_seeds.values(), *palestra_seeds.values()]:
        rows.append(
            {
                "id": seed["id"],
                "name": seed["name"],
                "creditMinutes": seed["creditMinutes"],
                "startDate": seed["startDate"],
                "endDate": seed["endDate"],
                "type": seed["type"],
                "emoji": seed["emoji"],
                "description": seed["description"],
                "shortDescription": seed["shortDescription"],
                "latitude": None,
                "longitude": None,
                "locationDescription": seed["locationDescription"],
                "majorEventId": seed["majorEventId"],
                "eventGroupId": event_group_id_by_type.get(seed["type"]),
                "allowSubscription": True,
                "slots": seed["slots"],
                "shouldIssueCertificate": False,
                "shouldCollectAttendance": True,
                "isOnlineAttendanceAllowed": False,
                "onlineAttendanceCode": None,
                "onlineAttendanceStartDate": None,
                "onlineAttendanceEndDate": None,
                "publiclyVisible": True,
                "youtubeCode": None,
                "buttonText": None,
                "buttonLink": None,
                "createdAt": seed["createdAt"] or fallback_now,
                "createdById": None,
                "updatedAt": fallback_now,
            }
        )

    return sorted(rows, key=lambda item: item["id"])


def build_major_event_rows(
    ano_rows: list[dict[str, Any]],
    major_event_id_by_year_ref: dict[int, str],
    event_rows: list[dict[str, Any]],
    paid_by_major_event: dict[str, bool],
    fallback_now: datetime,
) -> list[dict[str, Any]]:
    date_ranges: dict[str, tuple[datetime, datetime]] = {}
    for event in event_rows:
        major_event_id = event.get("majorEventId")
        start_date = event.get("startDate")
        end_date = event.get("endDate")
        if major_event_id is None or start_date is None or end_date is None:
            continue
        existing = date_ranges.get(major_event_id)
        if existing is None:
            date_ranges[major_event_id] = (start_date, end_date)
            continue
        date_ranges[major_event_id] = (min(existing[0], start_date), max(existing[1], end_date))

    rows: list[dict[str, Any]] = []
    for row in ano_rows:
        year_ref_id = decimal_to_int(row.get("idAno_Referencia"))
        year = decimal_to_int(row.get("ano"))
        if year_ref_id is None:
            continue
        major_event_id = major_event_id_by_year_ref.get(year_ref_id)
        if major_event_id is None:
            continue

        date_range = date_ranges.get(major_event_id)
        default_start = at_start_of_day(date(year or 2000, 1, 1))
        default_end = at_start_of_day(date(year or 2000, 12, 31))
        start_date = date_range[0] if date_range else default_start or fallback_now
        end_date = date_range[1] if date_range else default_end or start_date
        if end_date < start_date:
            end_date = start_date

        rows.append(
            {
                "id": major_event_id,
                "name": f"SECOMPP {year}" if year else f"SECOMPP ano {year_ref_id}",
                "startDate": start_date,
                "endDate": end_date,
                "description": "Importado de secompp2.sql",
                "isPaymentRequired": paid_by_major_event.get(major_event_id, False),
                "createdAt": parse_mysql_datetime(row.get("create_time")) or start_date,
                "updatedAt": fallback_now,
            }
        )

    return sorted(rows, key=lambda item: item["id"])


