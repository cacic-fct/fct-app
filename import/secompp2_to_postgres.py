#!/usr/bin/env python3
"""
Import legacy secompp2.sql data into the current PostgreSQL schema.

This importer only migrates relevant legacy entities:
- ano_referencia -> major_events
- minicurso + palestra -> events
- dados -> people
- inscricao -> major_event_subscriptions
- participacao_minicurso + participacao_palestra -> event_subscriptions
- participacao_minicurso(presenca=1) -> event_attendances
- ministrante_minicurso + palestra.CPFusuarioFK -> event_lecturers

IDs generated by this importer are prefixed with: SYSCOMP-2-
People IDs are generated as UUIDv7-style values.
"""

from __future__ import annotations

import argparse
from dataclasses import dataclass
from datetime import date, datetime
from pathlib import Path
from typing import Any

from legacy_sql_import_utils import (
    at_start_of_day,
    build_prefixed_id,
    coerce_text,
    combine_date_and_time,
    decimal_to_int,
    deterministic_prefixed_id,
    normalize_cpf,
    normalize_email,
    normalize_person_name,
    parse_insert_rows_by_table,
    parse_mysql_date,
    parse_mysql_datetime,
    parse_mysql_time,
    strip_html,
    utc_now,
    uuid7_string,
)

PREFIX = "SYSCOMP-2-"

REQUIRED_TABLES = {
    "ano_referencia",
    "dados",
    "inscricao",
    "minicurso",
    "ministrante_minicurso",
    "palestra",
    "participacao_minicurso",
    "participacao_palestra",
}


@dataclass(slots=True)
class MigrationPayload:
    major_events: list[dict[str, Any]]
    event_groups: list[dict[str, Any]]
    events: list[dict[str, Any]]
    people: list[dict[str, Any]]
    major_event_subscriptions: list[dict[str, Any]]
    event_subscriptions: list[dict[str, Any]]
    event_attendances: list[dict[str, Any]]
    event_lecturers: list[dict[str, Any]]
    skipped_major_subscriptions: int
    skipped_event_subscriptions: int
    skipped_attendances: int
    skipped_lecturers: int


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Import legacy secompp2.sql data into PostgreSQL schema."
    )
    parser.add_argument(
        "--input",
        type=Path,
        default=Path("import/secompp2.sql"),
        help="Path to secompp2 SQL dump file.",
    )
    parser.add_argument(
        "--database-url",
        type=str,
        default="",
        help="Full PostgreSQL URL. If omitted, individual --db-* options are used.",
    )
    parser.add_argument("--db-host", type=str, default="localhost")
    parser.add_argument("--db-port", type=int, default=5432)
    parser.add_argument("--db-name", type=str, default="postgres")
    parser.add_argument("--db-user", type=str, default="postgres")
    parser.add_argument("--db-password", type=str, default="postgres")
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Build and report mapped rows without writing to database.",
    )
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    payload = build_payload(args.input)

    print(
        "Prepared rows -> "
        f"major_events={len(payload.major_events)}, "
        f"event_groups={len(payload.event_groups)}, "
        f"events={len(payload.events)}, "
        f"people={len(payload.people)}, "
        f"major_event_subscriptions={len(payload.major_event_subscriptions)}, "
        f"event_subscriptions={len(payload.event_subscriptions)}, "
        f"event_attendances={len(payload.event_attendances)}, "
        f"event_lecturers={len(payload.event_lecturers)}, "
        f"skipped_major_subscriptions={payload.skipped_major_subscriptions}, "
        f"skipped_event_subscriptions={payload.skipped_event_subscriptions}, "
        f"skipped_attendances={payload.skipped_attendances}, "
        f"skipped_lecturers={payload.skipped_lecturers}"
    )

    if args.dry_run:
        print("Dry run enabled. No database changes were made.")
        return

    database_url = args.database_url or (
        f"postgresql://{args.db_user}:{args.db_password}@"
        f"{args.db_host}:{args.db_port}/{args.db_name}"
    )
    write_payload(database_url, payload)
    print("Import completed.")


def build_payload(sql_path: Path) -> MigrationPayload:
    parsed = parse_insert_rows_by_table(sql_path, REQUIRED_TABLES)
    now = utc_now()

    major_event_id_by_year_ref = map_major_event_ids(parsed["ano_referencia"])
    event_group_rows = build_event_groups(now)
    event_group_id_by_type = {row["type"]: row["id"] for row in event_group_rows}

    minicurso_seeds = map_minicursos(parsed["minicurso"], major_event_id_by_year_ref, now)
    palestra_seeds = map_palestras(parsed["palestra"], major_event_id_by_year_ref, now)

    all_referenced_cpfs = collect_referenced_cpfs(parsed, palestra_seeds)
    people_rows, cpf_to_person_id = map_people(parsed["dados"], all_referenced_cpfs, now)

    (
        major_event_subscription_rows,
        skipped_major_subscriptions,
        paid_by_major_event,
    ) = map_major_event_subscriptions(
        parsed["inscricao"], cpf_to_person_id, major_event_id_by_year_ref, now
    )

    (
        event_subscription_rows,
        skipped_event_subscriptions,
    ) = map_event_subscriptions(
        parsed["participacao_minicurso"],
        parsed["participacao_palestra"],
        cpf_to_person_id,
        minicurso_seeds,
        palestra_seeds,
        now,
    )

    (
        event_attendance_rows,
        skipped_attendances,
    ) = map_event_attendances(parsed["participacao_minicurso"], cpf_to_person_id, minicurso_seeds, now)

    (
        event_lecturer_rows,
        skipped_lecturers,
    ) = map_event_lecturers(
        parsed["ministrante_minicurso"],
        palestra_seeds,
        cpf_to_person_id,
        minicurso_seeds,
        now,
    )

    event_rows = build_event_rows(
        minicurso_seeds,
        palestra_seeds,
        event_group_id_by_type,
        now,
    )

    major_event_rows = build_major_event_rows(
        parsed["ano_referencia"],
        major_event_id_by_year_ref,
        event_rows,
        paid_by_major_event,
        now,
    )

    return MigrationPayload(
        major_events=major_event_rows,
        event_groups=event_group_rows,
        events=event_rows,
        people=people_rows,
        major_event_subscriptions=major_event_subscription_rows,
        event_subscriptions=event_subscription_rows,
        event_attendances=event_attendance_rows,
        event_lecturers=event_lecturer_rows,
        skipped_major_subscriptions=skipped_major_subscriptions,
        skipped_event_subscriptions=skipped_event_subscriptions,
        skipped_attendances=skipped_attendances,
        skipped_lecturers=skipped_lecturers,
    )


def map_major_event_ids(ano_rows: list[dict[str, Any]]) -> dict[int, str]:
    mapping: dict[int, str] = {}
    for row in ano_rows:
        year_ref_id = decimal_to_int(row.get("idAno_Referencia"))
        if year_ref_id is None:
            continue
        mapping[year_ref_id] = build_prefixed_id(PREFIX, "major-event", "ano", year_ref_id)
    return mapping


def map_minicursos(
    legacy_rows: list[dict[str, Any]],
    major_event_id_by_year_ref: dict[int, str],
    fallback_now: datetime,
) -> dict[int, dict[str, Any]]:
    rows: dict[int, dict[str, Any]] = {}

    for row in legacy_rows:
        minicurso_id = decimal_to_int(row.get("idMinicurso"))
        year_ref_id = decimal_to_int(row.get("idAno_ReferenciaFK"))
        if minicurso_id is None or year_ref_id is None:
            continue
        major_event_id = major_event_id_by_year_ref.get(year_ref_id)
        if major_event_id is None:
            continue

        event_date = parse_mysql_date(row.get("data"))
        start_time = parse_mysql_time(row.get("hora_inicio"))
        end_time = parse_mysql_time(row.get("hora_termino"))
        start_date = combine_date_and_time(event_date, start_time) or at_start_of_day(event_date) or fallback_now
        end_date = combine_date_and_time(event_date, end_time) or start_date
        if end_date < start_date:
            end_date = start_date

        workload_hours = decimal_to_int(row.get("cargahoraria"))

        rows[minicurso_id] = {
            "id": build_prefixed_id(PREFIX, "event", "minicurso", minicurso_id),
            "name": coerce_text(row.get("nome"), fix_mojibake=True) or f"Minicurso {minicurso_id}",
            "type": "MINICURSO",
            "emoji": "🧪",
            "description": coerce_text(row.get("descricao"), fix_mojibake=True),
            "shortDescription": strip_html(row.get("descricao")),
            "creditMinutes": (workload_hours * 60) if workload_hours is not None else None,
            "startDate": start_date,
            "endDate": end_date,
            "locationDescription": coerce_text(row.get("local"), fix_mojibake=True),
            "majorEventId": major_event_id,
            "slots": decimal_to_int(row.get("vagas")),
            "createdAt": parse_mysql_datetime(row.get("create_time")) or start_date,
            "speakerCpf": None,
        }

    return rows


